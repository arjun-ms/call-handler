"""
Evaluate voice attribute inference against CREMA-D ground truth.

Reads metadata.tsv (generated by download_cremad_subset.py) and sends
each audio file to the /analyze endpoint, then computes:
  - Gender accuracy
  - Age bracket accuracy (within ±1 bracket tolerance)
  - Per-bracket breakdown
  - Confidence calibration stats

Usage:
    python scripts/eval_cremad.py \
        --dir tests/input-audio-files/CREMA-D \
        --url http://localhost:8000/analyze
"""
import argparse
import asyncio
import csv
import json
import os
import sys
import time
from pathlib import Path
from typing import Dict, List

import httpx


# ── Age bracket utilities ──────────────────────────────────────────────
BRACKETS_ORDERED = ["18-30", "31-45", "46-60", "60+"]


def bracket_index(bracket: str) -> int:
    """Return index of bracket in BRACKETS_ORDERED, or -1 if unknown."""
    try:
        return BRACKETS_ORDERED.index(bracket)
    except ValueError:
        return -1


def age_to_model_bracket(age: int) -> str:
    """Map exact age to model bracket."""
    if age < 30:
        return "18-30"
    elif age < 45:
        return "31-45"
    elif age < 60:
        return "46-60"
    else:
        return "60+"


# ── API inference ──────────────────────────────────────────────────────
async def infer_sample(
    client: httpx.AsyncClient,
    api_url: str,
    filepath: str,
    semaphore: asyncio.Semaphore,
) -> Dict:
    """Send a single audio file to the API and return the result."""
    async with semaphore:
        if not os.path.exists(filepath):
            return {"error": f"File not found: {filepath}"}
        try:
            with open(filepath, "rb") as f:
                resp = await client.post(
                    api_url,
                    files={"file": (os.path.basename(filepath), f, "audio/wav")},
                )
            if resp.status_code == 200:
                return resp.json()
            return {"error": f"HTTP {resp.status_code}: {resp.text[:200]}"}
        except Exception as e:
            return {"error": str(e)}


# ── Main evaluation ───────────────────────────────────────────────────
async def evaluate(
    dataset_dir: str,
    api_url: str,
    max_samples: int = 0,
    concurrency: int = 5,
):
    """Run evaluation against CREMA-D metadata.tsv."""
    tsv_path = os.path.join(dataset_dir, "metadata.tsv")
    if not os.path.exists(tsv_path):
        print(f"ERROR: metadata.tsv not found in {dataset_dir}")
        print("Run download_cremad_subset.py first.")
        sys.exit(1)

    # Load ground truth
    samples = []
    with open(tsv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="\t")
        for row in reader:
            samples.append(row)
            if max_samples > 0 and len(samples) >= max_samples:
                break

    print(f"Loaded {len(samples)} ground-truth samples from metadata.tsv")
    print(f"API endpoint: {api_url}")
    print(f"Concurrency: {concurrency}")
    print("-" * 60)

    # Run inference
    semaphore = asyncio.Semaphore(concurrency)
    async with httpx.AsyncClient(timeout=120.0) as client:
        tasks = []
        for s in samples:
            filepath = os.path.join(dataset_dir, s["filename"])
            tasks.append(infer_sample(client, api_url, filepath, semaphore))

        start = time.time()
        results = await asyncio.gather(*tasks)
        elapsed = time.time() - start

    print(f"Inference completed in {elapsed:.1f}s ({elapsed/len(samples):.2f}s/sample)")
    print("-" * 60)

    # Compute metrics
    compute_metrics(samples, results)

    # Save detailed results
    import datetime
    dataset_name = os.path.basename(os.path.normpath(dataset_dir)).lower()
    timestamp = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = f"eval_results_{dataset_name}_{timestamp}.json"
    results_path = os.path.join(dataset_dir, filename)
    detailed = []
    for s, r in zip(samples, results):
        detailed.append({"ground_truth": s, "prediction": r})
    with open(results_path, "w") as f:
        json.dump(detailed, f, indent=2)
    print(f"\nDetailed results saved to: {results_path}")


def compute_metrics(samples: List[Dict], results: List[Dict]):
    """Compute and print accuracy metrics."""
    gender_correct = 0
    age_exact = 0
    age_within_1 = 0
    total_valid = 0
    errors = 0

    # Per-bracket stats
    bracket_stats = {b: {"total": 0, "correct": 0} for b in BRACKETS_ORDERED}
    gender_stats = {"male": {"total": 0, "correct": 0}, "female": {"total": 0, "correct": 0}}

    # Confidence tracking
    gender_confidences = []

    per_sample_log = []

    for sample, result in zip(samples, results):
        if "error" in result:
            errors += 1
            per_sample_log.append(
                f"  - {sample['filename']}: ERROR - {result['error'][:80]}"
            )
            continue

        total_valid += 1
        true_gender = sample["gender"]
        true_age = int(sample["age"])
        true_bracket = age_to_model_bracket(true_age)

        # Extract predictions - handle different API response shapes
        # The /analyze endpoint returns a complex structure; adapt to actual format
        pred_gender, pred_bracket, gender_conf = extract_predictions(result)

        if pred_gender is None or pred_bracket is None:
            errors += 1
            per_sample_log.append(
                f"  - {sample['filename']}: Could not parse prediction"
            )
            continue

        pred_gender_lower = pred_gender.lower()

        # Gender accuracy
        g_match = pred_gender_lower == true_gender
        if g_match:
            gender_correct += 1
        gender_stats[true_gender]["total"] += 1
        if g_match:
            gender_stats[true_gender]["correct"] += 1

        # Age bracket accuracy
        exact = pred_bracket == true_bracket
        if exact:
            age_exact += 1

        # Within ±1 bracket tolerance
        pred_idx = bracket_index(pred_bracket)
        true_idx = bracket_index(true_bracket)
        within_1 = False
        if pred_idx != -1 and true_idx != -1:
            within_1 = abs(pred_idx - true_idx) <= 1
        if within_1:
            age_within_1 += 1

        # Per-bracket
        bracket_stats[true_bracket]["total"] += 1
        if exact:
            bracket_stats[true_bracket]["correct"] += 1

        # Confidence
        if gender_conf is not None:
            gender_confidences.append(gender_conf)

        # Log
        status = "+" if g_match else "-"
        per_sample_log.append(
            f"  {status} {sample['filename']}: "
            f"gender={true_gender}/{pred_gender_lower}{'+' if g_match else '-'} "
            f"age={true_age}({true_bracket})/{pred_bracket}{'+' if exact else '~' if within_1 else '-'}"
        )

    # Print per-sample log
    print("\nPer-sample results:")
    for line in per_sample_log:
        print(line)

    # Summary
    print("\n" + "=" * 60)
    print("EVALUATION SUMMARY")
    print("=" * 60)

    if total_valid == 0:
        print("No valid predictions to evaluate!")
        return

    print(f"Total samples: {len(samples)}")
    print(f"Successful predictions: {total_valid}")
    print(f"Errors/skipped: {errors}")
    print()

    # Gender
    g_acc = gender_correct / total_valid
    print(f"Gender Accuracy: {g_acc:.1%} ({gender_correct}/{total_valid})")
    for g in ["male", "female"]:
        s = gender_stats[g]
        if s["total"] > 0:
            print(f"  {g.capitalize()}: {s['correct']}/{s['total']} ({s['correct']/s['total']:.1%})")
    print()

    # Age
    a_acc = age_exact / total_valid
    a_tol = age_within_1 / total_valid
    print(f"Age Bracket Accuracy (exact): {a_acc:.1%} ({age_exact}/{total_valid})")
    print(f"Age Bracket Accuracy (±1):    {a_tol:.1%} ({age_within_1}/{total_valid})")
    print()

    # Per-bracket breakdown
    print("Per-bracket breakdown:")
    for b in BRACKETS_ORDERED:
        s = bracket_stats[b]
        if s["total"] > 0:
            print(f"  {b:>6}: {s['correct']}/{s['total']} ({s['correct']/s['total']:.1%})")
        else:
            print(f"  {b:>6}: (no samples)")

    # Confidence calibration
    if gender_confidences:
        avg_conf = sum(gender_confidences) / len(gender_confidences)
        print(f"\nMean gender confidence: {avg_conf:.3f}")
        print(f"Min/Max: {min(gender_confidences):.3f} / {max(gender_confidences):.3f}")


def extract_predictions(result: Dict):
    """
    Extract gender prediction, age bracket prediction, and confidence from API response.
    Handles /analyze, speakers list, and /v1/infer response shapes.
    """
    pred_gender = None
    pred_bracket = None
    gender_conf = None

    # Shape 1: Direct gender/age_bracket response (FastAPI response)
    if "gender" in result and isinstance(result["gender"], dict):
        pred_gender = result["gender"].get("prediction")
        gender_conf = result["gender"].get("confidence")
        # Try "age_bracket" first, then fallback to "age"
        if "age_bracket" in result and isinstance(result["age_bracket"], dict):
            pred_bracket = result["age_bracket"].get("prediction")
        elif "age" in result and isinstance(result["age"], dict):
            pred_bracket = result["age"].get("prediction")

    # Shape 2: Response containing speakers list
    elif "speakers" in result:
        speakers = result.get("speakers", [])
        if speakers:
            speaker = speakers[0]
            pred_gender = speaker.get("gender", {}).get("prediction")
            gender_conf = speaker.get("gender", {}).get("confidence")
            pred_bracket = speaker.get("age_bracket", {}).get("prediction") or speaker.get("age", {}).get("prediction")

    # Shape 3: Flat response
    elif "predicted_gender" in result:
        pred_gender = result["predicted_gender"]
        pred_bracket = result.get("predicted_age_bracket") or result.get("predicted_age")

    return pred_gender, pred_bracket, gender_conf


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Evaluate voice inference against CREMA-D ground truth"
    )
    parser.add_argument(
        "--dir",
        type=str,
        default="tests/input-audio-files/CREMA-D",
        help="Directory with CREMA-D audio files and metadata.tsv",
    )
    parser.add_argument(
        "--url",
        type=str,
        default="http://localhost:8000/analyze",
        help="API endpoint URL",
    )
    parser.add_argument(
        "--n",
        type=int,
        default=0,
        help="Max samples to evaluate (0 = all)",
    )
    parser.add_argument(
        "--concurrency",
        type=int,
        default=5,
        help="Max concurrent API requests",
    )
    args = parser.parse_args()
    asyncio.run(evaluate(args.dir, args.url, args.n, args.concurrency))
